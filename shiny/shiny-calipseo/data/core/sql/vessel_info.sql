SELECT v.REGISTRATION_NUMBER, 
vt.I18n_DEFAULT AS VESSEL_TYPE, 
st.I18n_DEFAULT AS VESSEL_STAT_TYPE , 
ls.I18n_DEFAULT AS HOME_PORT_LANDING_SITE, 
ls2.I18n_DEFAULT AS REG_PORT_LANDING_SITE, 
ve.MANUFACTURER, 
et.I18n_DEFAULT AS ENGINE_TYPE, 
nrj.I18n_DEFAULT AS ENERGY_TYPE 
FROM reg_vessels AS v 
LEFT JOIN (SELECT * FROM cl_ref_vessel_types ) AS vt ON v.CL_REF_VESSEL_TYPE_ID = vt.ID 
LEFT JOIN (SELECT * FROM cl_app_vessel_stat_type) AS st ON v.CL_APP_VESSEL_STAT_TYPE_ID = st.ID 
LEFT JOIN (SELECT * FROM cl_fish_landing_sites) AS ls ON v.CL_FISH_HOME_PORT_LANDING_SITE_ID = ls.ID 
LEFT JOIN (SELECT * FROM cl_fish_landing_sites) AS ls2 ON v.CL_FISH_REG_PORT_LANDING_SITE_ID = ls2.ID 
LEFT JOIN (SELECT * FROM reg_vessel_engines) AS ve ON v.ID = ve.REG_VESSEL_ID 
LEFT JOIN (SELECT * FROM cl_app_engine_types) AS et ON ve.CL_APP_ENGINE_TYPE_ID = et.ID 
LEFT JOIN (SELECT * FROM cl_app_energy_types) AS nrj ON ve.CL_APP_ENERGY_TYPE_ID = nrj.ID 